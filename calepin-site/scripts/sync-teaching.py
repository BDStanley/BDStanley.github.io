"""Generate the benstanley.eu Teaching pages from the Teaching repo.

READ-ONLY with respect to the Teaching repo (BDStanley/Classes). It parses the
Quarto sources there and writes Typst pages into THIS site:

    pages/teaching.typ              the landing page (Heiss-style flat course list)
    pages/teaching/<slug>.typ       one page per course (slide/handout link lists)

The per-course pages list the same links/stubs as the Teaching repo's `.qmd`
files; each link points at the already-rendered artifact on the Netlify
teaching site (https://bdstanley.netlify.app/<stub>). Nothing is copied or
re-rendered — only the small index/course markdown is read.

build.sh runs this before `calepin compile`, so editing the Teaching repo and
rebuilding benstanley.eu reflects the changes here. The Teaching repo location
and the Netlify base URL can be overridden with env vars TEACHING_SRC and
TEACHING_NETLIFY_BASE.
"""

